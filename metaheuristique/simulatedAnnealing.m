villes = cell2mat(maroc_villes(:, 2:3));



% Calcul de la distance entre chaque deux villes
n = size(villes, 1);
distances = zeros(n, n);
for i = 1:n
    for j = 1:n
        distances(i, j) = norm(villes(i, :) - villes(j, :));
    end
end

% initialisation de configuration 
temp_initiale = 100; % T initial
alpha = 0.99; % Taux de reefroidissement
k = 10000; % Nombre d'itérations

% Initialisation de la solution
solution_courante = randperm(n); % engendrer une config initian S0.
cout_courant = calculer_cout(solution_courante, distances); % f(s)

% Boucle principale du recuit simulé
temp = temp_initiale;
for iter = 1:k
    % engendrer une voisinage aleatoire S'
    nouvelle_solution = solution_courante;
    ville_index_1 = randi(n);
    ville_index_2 = randi(n);
    nouvelle_solution([ville_index_1, ville_index_2]) = nouvelle_solution([ville_index_2, ville_index_1]);
    
    % Calculer le coût de la nouvelle solution f(s')
    nouveau_cout = calculer_cout(nouvelle_solution, distances);
    
    
    if nouveau_cout < cout_courant
        solution_courante = nouvelle_solution;
        cout_courant = nouveau_cout;
    else
        % Calculer la probabilité d'acceptation selon la température
        probabilite_acceptation = exp(-(nouveau_cout - cout_courant) / temp);
        % Accepter la nouvelle solution avec cette probabilité
        if rand < probabilite_acceptation
            solution_courante = nouvelle_solution;
            cout_courant = nouveau_cout;
        end
    end
    
    % Refroidir le système
    temp = temp * alpha;
end

